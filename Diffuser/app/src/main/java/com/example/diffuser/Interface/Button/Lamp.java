package com.example.diffuser.Interface.Button;

public class Lamp {
    private int r=255,g=255,b=255,bright=255;

    public void turnOn(int r,int g,int b){
        r= r;
        g= g;
        b= b;
    }
    public void turnOn(int r,int g,int b,int bright){
        r= r;
        g= g;
        b= b;
        bright = bright;
    }

    public void turnOff(int r,int g,int b){
        r= r;
        g= g;
        b= b;
    }
    public void turnOff(int r,int g,int b,int bright){
        r= r;
        g= g;
        b= b;
        bright = bright;
    }
}
