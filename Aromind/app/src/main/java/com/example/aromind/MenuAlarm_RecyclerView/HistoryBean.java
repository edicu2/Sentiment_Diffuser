package com.example.aromind.MenuAlarm_RecyclerView;

public class HistoryBean {

    private String set_am_pm, set_Tiem, set_day, set_alarm_name;
    private int set_hour, set_min;
    private boolean[] set_week;

    public String getSet_am_pm() {
        return set_am_pm;
    }

    public void setSet_am_pm(String set_am_pm) {
        this.set_am_pm = set_am_pm;
    }

    public String getSet_Tiem() {
        return set_Tiem;
    }

    public void setSet_Tiem(String set_Tiem) {
        this.set_Tiem = set_Tiem;
    }

    public String getSet_day() {
        return set_day;
    }

    public void setSet_day(String set_day) {
        this.set_day = set_day;
    }

    public String getSet_alarm_name() {
        return set_alarm_name;
    }

    public void setSet_alarm_name(String set_alarm_name) {
        this.set_alarm_name = set_alarm_name;
    }

    public int getSet_hour() {
        return set_hour;
    }

    public void setSet_hour(int set_hour) {
        this.set_hour = set_hour;
    }

    public int getSet_min() {
        return set_min;
    }

    public void setSet_min(int set_min) {
        this.set_min = set_min;
    }

    public boolean[] getSet_week() {
        return set_week;
    }

    public void setSet_week(boolean[] set_week) {
        this.set_week = set_week;
    }
}
